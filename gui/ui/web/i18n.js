  let locale = 'es';

  function t(key, vars) {
    const bag = STRINGS[locale] || STRINGS.es;
    let s = bag[key] ?? STRINGS.es[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
      });
    }
    return s;
  }

  function setLocale(next) {
    locale = STRINGS[next] ? next : 'es';
    document.documentElement.lang = locale;
    applyI18n();
    document.dispatchEvent(new CustomEvent('heimdall:locale', { detail: locale }));
    return locale;
  }

  function getLocale() {
    return locale;
  }

  function applyI18n(root) {
    const scope = root || document;
    scope.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      if (key) el.textContent = t(key);
    });
    scope.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (key) el.placeholder = t(key);
    });
    scope.querySelectorAll('[data-i18n-title]').forEach((el) => {
      const key = el.getAttribute('data-i18n-title');
      if (key) el.title = t(key);
    });
    scope.querySelectorAll('option[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      if (key) el.textContent = t(key);
    });
  }

  global.HeimdallI18n = { t, setLocale, getLocale, applyI18n, STRINGS };
